	protected static final Map<String, MCMaterial> BY_STRING = new HashMap<>();

	public MCMaterial(MCVanillaMaterial mcVanillaMaterial, Concrete concrete) {
		super(mcVanillaMaterial, concrete);
	}

	/**
	 * Replicates Enum.valueOf().
	 *
	 * @param materialName To test against our current values.
	 * @return Matching material object
	 * @throws IllegalArgumentException if material is not found by that name
	 */
	public static MCMaterial valueOf(String materialName) throws IllegalArgumentException {
		MCMaterial ret = get(materialName);
		if(ret == null) {
			throw new IllegalArgumentException("Unknown material type: " + materialName);
		}
		return ret;
	}

	/**
	 * @param materialName To test against our current values.
	 * @return Matching material object or null
	 */
	public static MCMaterial get(String materialName) {
		return BY_STRING.get(materialName);
	}

	public static Set<String> types() {
		if(BY_STRING.isEmpty()) { // docs mode
			Set<String> dummy = new HashSet<>();
			for(final MCVanillaMaterial t : MCVanillaMaterial.values()) {
				if(t.existsIn(MCVersion.CURRENT)) {
					dummy.add(t.name());
				}
			}
			return dummy;
		}
		return BY_STRING.keySet();
	}

	/**
	 * Reflectively accessed by NativeTypeList!
	 *
	 * @return Our own MCMaterial list
	 */
	public static List<MCMaterial> values() {
		if(BY_STRING.isEmpty()) {
			ArrayList<MCMaterial> dummy = new ArrayList<>();
			for(final MCVanillaMaterial t : MCVanillaMaterial.values()) {
				if(!t.existsIn(MCVersion.CURRENT)) {
					continue;
				}
				dummy.add(new MCMaterial<>(t, null) {
					@Override
					public String name() {
						return t.name();
					}
				});
			}
			return dummy;
		}
		return new ArrayList<>(BY_STRING.values());
	}

