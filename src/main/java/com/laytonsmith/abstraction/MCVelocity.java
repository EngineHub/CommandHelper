package com.laytonsmith.abstraction;

public class MCVelocity{
    public double magnitute;
    public double x;
    public double y;
    public double z;
    public MCVelocity(double magnitute, double x, double y, double z){
        this.magnitute = magnitute;
        this.x = x;
        this.y = y;
        this.z = z;
    }
}